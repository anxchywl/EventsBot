// map backend error text to localized messages
export function translateError(message) {
  if (!message) {
    return "";
  }

  const errors = dict[state.lang]?.errors || {};
  const fallback = dict.en.errors || {};
  if (message in errors) {
    return errors[message];
  }
  if (message in fallback) {
    return fallback[message];
  }

  const attemptMatch = message.match(/^Invalid verification code\. Attempts remaining: (\d+)\.$/);
  if (attemptMatch) {
    const template = errors["Invalid verification code. Attempts remaining: {remaining}."] || fallback["Invalid verification code. Attempts remaining: {remaining}."] || "Invalid verification code. Attempts remaining: {remaining}.";
    return template.replace("{remaining}", attemptMatch[1]);
  }

  const waitMatch = message.match(/^Please wait (\d+) seconds before requesting a new code\.$/);
  if (waitMatch) {
    const template = errors["Please wait {seconds} seconds before requesting a new code."] || fallback["Please wait {seconds} seconds before requesting a new code."] || "Please wait {seconds} seconds before requesting a new code.";
    return template.replace("{seconds}", waitMatch[1]);
  }

  const registrationMatch = message.match(/^Too many registration attempts\. Please try again in (\d+) minutes\.$/);
  if (registrationMatch) {
    const template = errors["Too many registration attempts. Please try again in {minutes} minutes."] || fallback["Too many registration attempts. Please try again in {minutes} minutes."] || "Too many registration attempts. Please try again in {minutes} minutes.";
    return template.replace("{minutes}", registrationMatch[1]);
  }

  const loginMatch = message.match(/^Too many login attempts\. Please try again in (\d+) minutes\.$/);
  if (loginMatch) {
    const template = errors["Too many login attempts. Please try again in {minutes} minutes."] || fallback["Too many login attempts. Please try again in {minutes} minutes."] || "Too many login attempts. Please try again in {minutes} minutes.";
    return template.replace("{minutes}", loginMatch[1]);
  }

  return message;
}

// translate known category names
export function categoryLabel(category) {
  const value = String(category || "").trim();
  const key = String(category || "").trim().toLowerCase().replace(/[\s-]+/g, "_");
  const translated = t(key);
  if (translated !== key) {
    return translated;
  }
  return value
    .split(/([\s-]+)/)
    .map((part) => (/[\s-]+/.test(part) ? part : part.charAt(0).toUpperCase() + part.slice(1).toLowerCase()))
    .join("");
}

// format event dates for the active language
export function formatEventDate(event) {
  const value = new Date(`${event.date}T${event.time || "00:00"}:00`);
  if (Number.isNaN(value.getTime())) {
    return `${event.date} ${event.time}`;
  }
  return new Intl.DateTimeFormat(state.lang, {
    weekday: "short",
    month: "short",
    day: "numeric",
    hour: "2-digit",
    minute: "2-digit",
  }).format(value);
}

// format reminder offsets for the active language
export function formatReminderOffset(minutes) {
  const d = Math.floor(minutes / 1440);
  const h = Math.floor((minutes % 1440) / 60);
  const m = minutes % 60;
  const parts = [];
  if (d) parts.push(`${d}${t("timerLabelDays")}`);
  if (h) parts.push(`${h}${t("timerLabelHours")}`);
  if (m) parts.push(`${m}${t("timerLabelMinutes")}`);
  return parts.length ? parts.join(" ") : `0${t("timerLabelMinutes")}`;
}
